package Oops3;

public abstract class Movie {
	public abstract String heroName(String name);
	public String heroineName(String name1) {
		return "Heroine Name"+name1;
	}
	public String isBlockBuster(boolean a) {
		return "Is Block Buster"+a;
	}
	public String isDoubleAct(boolean b) {
		return "Is Double Act"+b;
	}

}
