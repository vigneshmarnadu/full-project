package Oops1;

public class Engine {
	String brand;
	int price;
	int noOfPistons;
	
	public void setBrand(String brand) {
		this.brand=brand;
	}
	public String getBrand() {
		return brand;
	}
	public void setPrice(int price) {
		this.price=price;
	}
	public int getPrice() {
		return price;
	}
	public void setNoOfPistons(int noOfPistons) {
		this.noOfPistons=noOfPistons;
	}
	public int getNoOfPistons() {
		return noOfPistons;
	}
	

}
